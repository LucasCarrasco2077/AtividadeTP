var operacoes = {
    soma: function (NumA,NumB){
        return NumA+NumB;
    },
    mult: function(NumA,NumB){
        return NumA*NumB;
    },
    subt: function (NumA,NumB){
        return NumA-NumB;
    },
    div: function (NumA,NumB){
        return NumA/NumB;
    }
};

module.exports= operacoes;